<template>
  <ul class="isc-select-group__wrap" v-show="visible">
    <li class="isc-select-group__title">{{ label }}</li>
    <li>
      <ul class="isc-select-group">
        <slot></slot>
      </ul>
    </li>
  </ul>
</template>

<script type="text/babel">
  import Emitter from 'isyscore-ui/src/mixins/emitter';

  export default {
    mixins: [Emitter],

    name: 'IsOptionGroup',

    componentName: 'IsOptionGroup',

    props: {
      label: String,
      disabled: {
        type: Boolean,
        default: false
      }
    },

    data() {
      return {
        visible: true
      };
    },

    watch: {
      disabled(val) {
        this.broadcast('IsOption', 'handleGroupDisabled', val);
      }
    },

    methods: {
      queryChange() {
        this.visible = this.$children &&
          Array.isArray(this.$children) &&
          this.$children.some(option => option.visible === true);
      }
    },

    created() {
      this.$on('queryChange', this.queryChange);
    },

    mounted() {
      if (this.disabled) {
        this.broadcast('IsOption', 'handleGroupDisabled', this.disabled);
      }
    }
  };
</script>
